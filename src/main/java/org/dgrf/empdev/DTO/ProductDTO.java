package org.dgrf.empdev.DTO;

/**
 *
 * @author dgrfiv
 */
public class ProductDTO {
    private int Id;
    private String name;
    private int price;

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
}
